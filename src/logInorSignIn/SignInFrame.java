    private void btn_signinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signinActionPerformed
        // TODO add your handling code here:
        VoiceSpeech v = new VoiceSpeech();
        String user = txt_name.getText();
        String pass = txt_pass.getText();
        UserInformation u = null;
        String confirm = txt_confirmpass.getText();
        if (!pass.equals("") && user.length()>0 && confirm.length()>0) {
            if(pass.equals(confirm)){
                u = new UserInformation(user, pass);
                CSDLUsers cs = null;
                try {
                        cs = new CSDLUsers();
                        boolean f = cs.addSQL(u);
                        if (f){
                            JOptionPane.showMessageDialog(btn_signin, "SUCCEED!", "Message",
                                JOptionPane.INFORMATION_MESSAGE);
                            LogInFrame log = new LogInFrame();
                            log.setVisible(true);
                            dispose();
                        }

                        else {
                            JOptionPane.showMessageDialog(btn_signin, "FAIL! The username has already exist! Please create a new one!", "Error!",
                                            JOptionPane.ERROR_MESSAGE);
                        }
                } catch (SQLException e1) {
                    JOptionPane.showMessageDialog(btn_signin, "FAIL! Please try again!", "Error!",
                                            JOptionPane.ERROR_MESSAGE);
                }
            }else{
                v.speech("2", "Please confirm your password again!", 120);
            }
            
        } else if(user.length()<=0)
            v.speech("2", "Please type your username", 120);
            else if(pass.length()<=0)
                v.speech("2", "Please type your password", 120);
            else 
                v.speech("2", "Please confirm your password!", 120);
    }//GEN-LAST:event_btn_signinActionPerformed

