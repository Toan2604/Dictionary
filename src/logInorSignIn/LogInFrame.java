    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        VoiceSpeech v = new VoiceSpeech();
        ArrayList<UserInformation> list = new ArrayList<UserInformation>();
        list = CSDLUsers.findUser(txt_name.getText());
        boolean f = true;
        if(txt_name.getText().length()>0 && txt_pass.getText().length()>0){
            for (UserInformation u : list) {
                if(txt_name.getText().equals(u.getUser()) && txt_pass.getText().equals(u.getPass())){   
                    v.speech("1","Log in SUCCEED!",100);
                    Dictionary1 dic = new Dictionary1();
                    dic.setVisible(true);
                    dispose();
                    f = false;
                }
                
            }
            if(f)
            {
                v.speech("3","Invalid your name or password. Please try again!",100);

                txt_name.setText("");
                txt_pass.setText("");
            }
        }
        else{
            v.speech("2","Invalid your name or password. Please try again!",100);
           
            if(txt_name.getText().length()<=0)
                txt_name.setText("");
            if(txt_name.getText().length()<=0){
                txt_name.setText("");
                txt_pass.setText("");
            }
                
        }

    }//GEN-LAST:event_btn_loginActionPerformed

