    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        String word = txt_word.getText();
        String mean = txt_mean.getText();
        String exam = txt_exam.getText();
        if (!mean.equals("")) {
            Words w = new Words(word, mean, exam);
            KetNoiCSDL kn;
            try {
                    kn = new KetNoiCSDL();
                    boolean f = kn.addSQL(w);
                    if (f)
                            JOptionPane.showMessageDialog(btn_add, "Insert succeeded!", "Message",
                                            JOptionPane.INFORMATION_MESSAGE);
                    else {
                            JOptionPane.showMessageDialog(btn_add, "Insert failed", "Error!",
                                            JOptionPane.ERROR_MESSAGE);
                    }
            } catch (SQLException e1) {
                    JOptionPane.showMessageDialog(btn_add, "Insert failed", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(btn_add, "Insert failed", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_addActionPerformed

