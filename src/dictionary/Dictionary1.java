    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed
        // TODO add your handling code here:
        ArrayList<Words> list = new ArrayList<Words>();

        list = KetNoiCSDL.findWord(txt_input.getText());
        // showAble(); // show all data
        boolean f = true;
        for (Words w : list) {
            if (w.getWord().equalsIgnoreCase(txt_input.getText())) {
                txt_search.setText(null);
                txt_search.append(w.getWord());
                txt_search.append("\n");
                txt_search.append(w.getMean());
                txt_search.append("\n");
                txt_search.append(w.getExample());
                txt_search.append("\n");

                f = false;
            }
        }

        if (f) {
            int opt = JOptionPane.showConfirmDialog(btn_Search, "No this word in Dictionary! \nDo you want to insert it into your Dictionary?",
                            "Message", JOptionPane.YES_NO_OPTION);
            if (opt == 0) {
                AddWord adds = new AddWord();
                adds.setVisible(true);
                adds.txt_word.setText(txt_input.getText());
                
                dispose();
            }
        }
    }//GEN-LAST:event_btn_SearchActionPerformed

