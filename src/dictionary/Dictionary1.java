    private void btn_SpeakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SpeakActionPerformed
        // TODO add your handling code here:
        System.setProperty("freetts.voices", "de.dfki.lt.freetts.en.us.MbrolaVoiceDirectory");
        System.setProperty("mbrola.base", "D:\\My Study\\JAVA\\BTL_JAVA\\JAVA_speech\\mbrola");
        VoiceManager vm = VoiceManager.getInstance();
        Voice voice = vm.getVoice("mbrola_us1");
        try {
            voice.allocate();
            if(txt_input.getText().length()<=0)
                voice.speak("Can't read, please check again!");
            else
                voice.speak(txt_input.getText());
            
            voice.deallocate();
        } catch (Exception e) {
            voice.speak("Failed! Please check again!");
        }
    }//GEN-LAST:event_btn_SpeakActionPerformed

